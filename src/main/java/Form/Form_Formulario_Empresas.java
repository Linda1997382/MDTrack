    private void guardar() {
        try {
            Map<String, Object> datos = new HashMap<>();
            // ✅ USAR NOMBRES CORRECTOS SEGÚN LA BASE DE DATOS
            datos.put("nombre", nombreEmpresaTxt.getText());
            datos.put("nombre_responsable", nombreResponsableTxt.getText());
            datos.put("telefono_responsable", telResponsableTxt.getText());
            datos.put("correo_responsable", eMailResponsableTxt.getText());
            datos.put("direccion", ""); // Agregar campo dirección vacío

            boolean exito;
            if (modoEdicion && empresaId != null) {
                // MODO EDICIÓN - usar actualizarEmpresa
                exito = Modelo.Model_Empresas.actualizarEmpresa(empresaId, datos);
            } else {
                // MODO CREACIÓN - usar guardarEmpresa
                exito = Modelo.Model_Empresas.guardarEmpresa(datos);
            }

            if (exito) {
                String mensaje = modoEdicion ? "Empresa actualizada con éxito" : "Empresa guardada con éxito";
                JOptionPane.showMessageDialog(null, mensaje);
            } else {
                JOptionPane.showMessageDialog(null, "Error al guardar");
            }
        } catch (HeadlessException e) {
            System.err.println("Error: " + e.getMessage());
            JOptionPane.showMessageDialog(null, "Error al guardar");
        }
    }

