    private void configurarModo() {
        if (modoEdicion) {
            jLabel1.setText("Editar Empresa"); // Cambiar título
            lblGuardar.setText("Actualizar");   // Cambiar texto del botón
        } else {
            jLabel1.setText("Nueva Empresa");   // Título por defecto
            lblGuardar.setText("Guardar");      // Texto por defecto
        }
    }

    private void cargarDatosEmpresa() {
        if (empresaId != null) {
            Map<String, Object> datos = Modelo.Model_Empresas.obtenerEmpresaPorId(empresaId);
            if (datos != null) {
                nombreEmpresaTxt.setText((String) datos.get("nombre"));
                nombreResponsableTxt.setText((String) datos.get("nombre_responsable"));
                telResponsableTxt.setText((String) datos.get("telefono_responsable"));
                eMailResponsableTxt.setText((String) datos.get("correo_responsable"));
            }
        }
    }

